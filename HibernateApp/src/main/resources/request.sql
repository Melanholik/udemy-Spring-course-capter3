-- Person

DROP TABLE IF EXISTS Person;

CREATE TABLE IF NOT EXISTS Person
(
    id   int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(100) NOT NULL,
    age  INT CHECK ( age < 120 )
);

