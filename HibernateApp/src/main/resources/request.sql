FROM Person;

-- Item

DROP TABLE IF EXISTS Item;

CREATE TABLE IF NOT EXISTS Item
(
    id        int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    person_id INT          REFERENCES Person (id) ON DELETE SET NULL,
    item_name VARCHAR(100) NOT NULL
);

